package operadoresaritmeticos;

public class AbreviandoOperadoresAritmeticos {

	public static void main(String[] args) {
		
		int minhaIdade = 38;
		int suaIdade = 35;
		
		int totalIdades = minhaIdade + suaIdade + 100;
		System.out.println("===============================================================");
		System.out.println("Total das idades: " + totalIdades + " anos.");
		System.out.println("===============================================================");
		
		int diferencaIdades = minhaIdade - suaIdade;
		System.out.println("===============================================================");
		System.out.println("Diferença das idades: " + diferencaIdades + " anos.");
		System.out.println("===============================================================");
		
		
		int produtoIdades = minhaIdade * suaIdade;
		System.out.println("===============================================================");
		System.out.println("Multiplicação das idades: " + produtoIdades + " anos.");
		System.out.println("===============================================================");
		
		
		int divisaoIdades = minhaIdade / suaIdade;
		System.out.println("===============================================================");
		System.out.println("Divisão das idades: " + divisaoIdades + " anos.");
		System.out.println("===============================================================");
		
		
		int moduloIdades = minhaIdade % suaIdade;
		System.out.println("===============================================================");
		System.out.println("O resto (módulo) das idades: " + moduloIdades + " anos.");
		System.out.println("===============================================================");
		
		
		

	}

}
