package desafiovariaveiseoperadoresaritmeticos;

public class DesafioVariaveisEOperadoresAritmeticos {

	public static void main(String[] args) {
		
		double primeiraVenda = 20.0;
		double segundaVenda = 30.0;
		double terceiraVenda = 100.0;
		
		double somaVendas = primeiraVenda + segundaVenda + terceiraVenda; 
		
		double totalUltimasVendas = somaVendas / 3;
		
		System.out.println("%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%");
		System.out.println("A média das últimas vendas é R$ " + totalUltimasVendas);
		System.out.println("%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%");

	}

}
