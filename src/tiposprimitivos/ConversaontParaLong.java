package tiposprimitivos;

public class ConversaontParaLong {

	public static void main(String[] args) {
		
		
		int teste1 = 102344;
		long teste2 = (int) teste1;
		
		System.out.println("O valor do Teste 1 (que é int) é: " + teste1);
		System.out.println();
		System.out.println("O valor do Teste 2 (que é long) é: " + teste2);
		System.out.println();
		
		int teste3 = 930000035;
		long teste4 = (int) teste3;
		System.out.println("O valor do Teste 3 (que é int) é: " + teste3);
		System.out.println();
		System.out.println("O valor do Teste 2 (que é long) é: " + teste4); 
		
		
	}

}
